// 出力値ベース・テストは、テスト対象に入力値を渡し、その出力を検査します。フロントエンドでは、
// React のカスタムフックのテストに使われることがありますが、状態ベース・テストと組み合わせて使われることになります。
//（純粋な関数に対しても出力値ベース・テストを使いますが、それはフロントエンドに限った話ではないです。）

// 状態ベース・テスト
// 状態ベース・テストは、処理の実行によってテスト対象の状態がどうなっているかを検査します。
// フロントエンドのテストにおいては、ユーザのインタラクションによって「○○というテキストが表示されていること」
// や「△△ボタンが disabled になっていること」などを確認することが該当します。

// コミュニケーションベース・テスト
