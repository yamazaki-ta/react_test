import { render, screen } from '@testing-library/react'
import App from '../App'

describe('App Component', () => {
     it('renders the paragraph correctly', () => {
          render(<App />)
          const paragraphElement = screen.getByText('ここのテスト')
          expect(paragraphElement).toBeInTheDocument()
     })
})

